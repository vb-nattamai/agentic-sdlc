"""
InfrastructureAgent — generates Docker IaC files and runs docker compose.

Phase "plan": Generates all infrastructure files (Dockerfiles, docker-compose.yml,
              README.md, AGENTS.md, .cursorrules, .github/copilot-instructions.md).
Phase "apply": Runs docker compose up --build -d and polls health endpoints,
               returning a detailed apply_result with service status.
"""

from __future__ import annotations

import asyncio
import json
from pathlib import Path
from typing import Any

from agents.base_agent import BaseAgent
from models.artifacts import InfrastructureArtifact


